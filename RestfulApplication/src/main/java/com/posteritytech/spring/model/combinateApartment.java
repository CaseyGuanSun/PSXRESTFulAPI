package com.posteritytech.spring.model;

public class combinateApartment {

	private String apartment;
	private String extension;
	private String sipNumber;
	private String pstnNumber;
	private String password;
	private int groupId;
	public String getApartment() {
		return apartment;
	}
	public void setApartment(String apartment) {
		this.apartment = apartment;
	}
	public String getExtension() {
		return extension;
	}
	public void setExtension(String extension) {
		this.extension = extension;
	}
	public String getSipNumber() {
		return sipNumber;
	}
	public void setSipNumber(String sipNumber) {
		this.sipNumber = sipNumber;
	}
	public String getPstnNumber() {
		return pstnNumber;
	}
	public void setPstnNumber(String pstnNumber) {
		this.pstnNumber = pstnNumber;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public int getGroupId() {
		return groupId;
	}
	public void setGroupId(int groupId) {
		this.groupId = groupId;
	}
}
